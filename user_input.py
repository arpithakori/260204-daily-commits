user_input = input("Enter the text => lol, rofl, lmk, smh:\n")
if user_input == "lol":
    print("laughing out loud")
elif user_input == "rofl":
    print("rolling on the floor")
elif user_input == "lmk":
    print("let me know")
elif user_input == "smh":
    print("shake my head")
else:
    print("Invalid input")